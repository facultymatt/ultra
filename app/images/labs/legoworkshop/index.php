						<h3>A new LEGO theme</h3>
						<p>The current LEGO lineup consists of different themes. My favorite theme is, and always was, the City. The LEGO city is unique because it has many sub-themes including: Trains, Construction, Farm, Harbour, Airport, Space, and Construction. But something is missing… after living in the City (of Philadelphia, that is) for almost 2 years I have come across a place that is worthy, dare I go so far as to say, of it’s own theme! This is a place full of LEGO fanatics. It’s a place where you can create things with the building blocks of the real world. A place where you can cut and weld and drill and make robots. It’s called the fabrication shop.</p>
						
						
						<h3>The research</h3>
 						<p>This ongoing project is based on extensive research into LEGO toys and the LEGO System of Play that I discovered while working on my Master’s Thesis: Build Play Learn. This research explained how LEGO toys engage the imagination and help children to understand their world. LEGOs encourage children to role-play—helping keep the city safe, launching the space shuttle into orbit, and being a train engineer—the possibilities are endless!</p>

						<p>It’s also inspired by my childhood, where I spent countless hours building with LEGOs. I would spread the parts across the living room floor for days at a time. LEGOs taught me to follow instructions, looking closely at each step of the directions for new parts. The LEGO system also allowed me to build from my imagination. Perhaps most importantly, LEGOs helped me to understand how the world around me worked.</p> 
						
						<p>For example, building the LEGO train-set put me in the engineers seat. I learned the different elements of the train yard, and was inspired to inquire about trains in real life. I wondered if a lego fabrication shop do the same? Could it teach kids about the possibilities of new manufacturing methods such as the laser cutter, while also teaching them about the traditional shop tools? Could it get kids interested in making and building? These are questions that I continue to ask moving forward with this project.</p>
						
