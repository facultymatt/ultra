			<h3>How it Works</h3>
			<p>Rant cloud is a collaboration with Jacob Wells and Mike Smith created during the 2010 Baltimore Hackathon (three days of hacking and making). Rant cloud allows anyone to voice their opinion, to rant about a particular question or topic. Users call a special phone number and speak what's on their mind. Their voice is transcribed to text and then added to a collective bank of words. A word cloud is generated from this bank of words. The more often a word is said, the larger it appears in the word cloud. In this way, you can quickly see what people have to say about a topic.</p>
			<h3>Rant Cloud in Action</h3>
			<p>Rant cloud can be used in a variety of situations where it’s important that everyones’ opinion counts. This service is accessible to anyone who can make a telephone call, ensuring no opinion is left unheard. Rants that are off-topic or offensive, while not directly censored, quickly dissapear as more relevant words increase in frequency and size within the word cloud.</p>
