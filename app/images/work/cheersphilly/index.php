				<h4>Design Brief</h4>
				<div class="inner_wrapper">
					
					<h3>Philadelphia is a beer drinkers city, with more local brews and historical brewing “firsts” than any other. There is even a section of the city called Brewerytown, aptly named for the collection of breweries operating there during the late 19th and early 20th century. </h3>

					<p>The initial design brief, to create a campaign that would promote this local industry, led to countless hours of research, brewery tours, and talking with industry leaders and local beer drinkers alike. We discovered longstanding rivalries and secretly held recipes, debates over the superiority of the ales versus the lager, and learned firsthand how the brewing process works.</p>
					
					<p>Above all, it was clear that Philadelphians were passionate about two things: beer and history. Taking note from the <a href="http://buylocalpa.org/" rel="external" target="_blank" title="Visit the Buy Local PA campaign">Buy Local PA</a> campaign, which promotes locally grown food throughout the state, we focused on the industry and its history rather than specific manufacturers. In this way, we created a culture where everyone is welcome — from breweries, to bars and restaurants, right on down to the beer drinkers themselves! We focused on history from 1683 up until 1987, when Schmidt’s brewery, the city’s largest, closed its doors forever.</p>
				<h4>Project Highlights</h4>
				<div class="sevencol ">
					<h5>Website (public facing)</h5>
					<p class="details">The website introduces visitors to Philadelphia brewing history. It was designed to have the look and feel of an old newspaper, right down to the details. For example, as you move through history the price of the newspaper changes, from 1 cent in 1683 to 50 cents in 1987.</p> 
				</div>
				<div class="fivecol last"><img src="process/cheersphilly-website.jpg" title="" /></div>
