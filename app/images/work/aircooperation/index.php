					<h3>Air cooperation brings community members together through the universal language of music using a collaborative-wind-instrument. The instrument itself is a product of collaboration, created by a team of eight design students during the 2010 UArts Design Charrette.</h3>
					
					<p>The instrument consists of a foot-activated air-pump and PVC flute connected by thin plastic tubing. Each flute is tuned to play a single note and a full scale or song can be arranged with a series of these instruments. The flutes are mounted together in one location while their corresponding foot pedals are scattered throughout the environment. In this way multiple participants must operate the air-pumps in cooperation to play the full scale of notes. </p>
					
					<p>Since this instrument is low-tech (no electric needed) and made from rugged materials (PVC, industrial-grade pneumatic fittings) it’s well suited for the outdoors. My goal is to bring this musical experience to playgrounds across Philadelphia. Currently I am seeking funding and working to secure locations. Interested in helping? Please give me a shout (or play me a tune!)</p>
					
