				<h4>Design Brief</h4>
				<div class="inner_wrapper">
					<h3>Apple products have built-in screen reading software, called Voiceover, that makes them appealing to the visually impaired. This software verbalizes all actions, including menu selection and key presses, and reads aloud documents and web pages.</h3>
					<p>It’s amazing this technology comes standard on all Apple computers. When you consider that similar software from other manufactures costs upwards of $1000, It’s even more amazing that Voiceover also comes standard on the iPad and iPhone.</p>
					
					<img class="fivecol" src="process/tactileipad-typing.jpg" />
					<p>However, even with Voiceover I noticed that it was near impossible for a visually impaired person to type on the iPad. Compared to the iPhone, where the small screen makes it easier to find your bearings, the iPad’s larger size quickly becomes disorienting. One person that I talked with stated the iPad “is a completely visual device.” And they are right, the iPad eliminates all tactile qualities of a traditional keyboard, replacing it instead with a flat sheet of glass. </p>
					
					<p>These two prototypes offer different solutions to the problem. The first is more practical, a plastic overlay has cutouts for each key. “I like this because my fingers feel locked into place when I type,” one person said after using the device. The second is more hypothetical. It’s based on a patent from Apple describing small pins that raise up to define keys and retract when not needed.</p>
					
					<div class="sixcol last">
					<img src="process/tactileipad-patent-pins.jpg" />
					<p class="caption">Apple Inc's patent diagram shows an iPhone with a series of tiny pins.</p>
					</div>
					
					<div class="sixcol last">
					<img src="process/tactileipad-pins-close2.jpg" />
					<p class=" caption">I created this simple rendering in Sketchup to visualize how these pins would function.</p>
					</div>
					
					<p>While the second solution is likely many years away, the first is happening now. A fellow named Cliff Thier has created a similar device for the iPad and successfully raised funds on Kickstarter. If you're interested, learn more about Cliff’s project with the link below.</p>
					
					
